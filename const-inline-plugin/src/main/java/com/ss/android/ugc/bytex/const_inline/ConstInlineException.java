package com.ss.android.ugc.bytex.const_inline;

public class ConstInlineException  extends RuntimeException{
    public ConstInlineException() {
    }

    public ConstInlineException(String s) {
        super(s);
    }

    public ConstInlineException(String s, Throwable throwable) {
        super(s, throwable);
    }

    public ConstInlineException(Throwable throwable) {
        super(throwable);
    }

    public ConstInlineException(String s, Throwable throwable, boolean b, boolean b1) {
        super(s, throwable, b, b1);
    }
}
